                <h1>Заглавие</h1>
                <p>
                    В нашето село от край време са се играли хора и ръченици по празници и сватби. Това продължава и сега по различни поводи – общоселски празници, читалищни мероприятия, семейни сбирки , стига да засвири игрива музика. Това изкуство в Тетово се предава от поколение на поколение. Не минава нито едно събитие без да се тропне Дайчово хоро,ръченица, Пристъпванката, Крушовенско хоро, а    по-големите майстори-играчи играят и Беленската.</p>
                <p> 
                    Първият младежки танцов състав към читалището се създава през януари 1949г. от Йордан Несторов Събев с участници: Ангел Върбанов Трофилев, Йордан Стефанов Георгиев, Иван Мих. Даков, Стефанка Великова Григорова, Иванка Бонева Илиева и Иванка Савова Енчева.</p>
                <p> 
                    През 1956г. се сформира по-голям състав, който периодично се разраства и променя. Негов ръководител е Петър Петров Енчев / Джамбаза/. Участници са: Йордан Несторов, Иван Даков, Петкана Хр. Братованова, Никола Ферд. Иванов, Тончо Мар. Габаров, Петър Дим. Драгнев, Пенчо Тодоров Босев, Иван Анг. Курков, Йорданка Ник. Фердинандова, Минка Петрова Драгнева, Радка Пенчева Босева, Дамяна Иванова Куркова и Тодорка Анг. Пенкова. Този танцов състав съществува продължително време и прави особено впечатление с оригиналните си изпълнения.
                </p>
                <p>
                В последствие танцовата трупа се променя и подмладява. Включват се нови танцьори: Галина Йорд. Савова, Румяна Петр. Попова, Маринка Кошарова, Митка Несторова, Пенчо Георг. Кайсоев, Йордан Борисов Чакъров, Кръстю Цанков, Бонка Георг. Чакърова, Стефан Бор. Щатев, Петър Петров Драгнев. В различни периоди ръководители на танцовия състав са: Георги Хр. Иванов, Цветанка Георг. Николова, Стефка Русева, Румяна Баева, Стефан Бор. Щатев.
                </p>
                <p>
                За 75-годишнината на читалището / 1982г./ се сформира детски танцов състав с ръководител Цветанка Георгиева Николова, а за 90-та годишнина / през 1996г./ Към читалището се изграждат две танцови групи: ученическа и детска с ръководители  Бистра Стефанова Борисова и  Стефка Русева.
                </p>
