                <h1>Заглавие</h1>
                <p>
                    В последните години стана модерно да се танцуват народни танци и от възрастни, организирани в т.нар. клубове за народни танци. Такива клубове се сформират обикновено в големите градове, но тетовчани винаги са били далновидни и създават такъв клуб през 2011г. Наричат го с името „Анесица”. Първите самодейци – танцьори са: Милена Маринова, Ивелина Ангелова, Цанка Ангелова, Славена Савчева, Татяна Пенева, Деница Пламенова, Габриела Пламенова, Павлина Савова, Румяна Попова, Виолета Григорова, Виолета Минчева, Благовеста Илчева, Аделина Димова, Верка Алексиева, Деница Маринова , Ралица Маринова, Невяна Атанасова, Елица Петракиева, Павлина Андреева, Петинка Тончева  и др. Съставът бързо набира скорост – разучават се хора от различни области, ушиват се сценични костюми и през 2012г. има куража да се яви на първото издание на Русчуклийска среща, където се надпреварва наред с големи и утвърдени клубове и е единствения клуб от селско читалище. Резултатите не закъсняват – през 2015,2016г. печелят Наградата на кмета на град Русе. Много са изявите на танцьорите, особено атрактивни стават, когато в състава се включват и мъжете  – Венцислав Атанасов, Мартин Петков, Станимир Нешев, Александър Пламенов, Василий Димов. 

                </p>
                <p> 
                Не минава местен празник без тяхно участие, а наред с това и изяви на различни сцени и фестивали в България и чужбина. Ще изброя само някои от тях: „Златната гъдулка”, Национален фолклорен фестивал „Лудогорие” гр.Разград, Фолклорен фестивал на любителските клубове „ Море бушува, Обзор танцува”  /2014г./ гр.Обзор, V световен шампионат по фолклор  - Слънчев бряг / 2015г. – спечелен сребърен медал/,ФФ „От Дунав до Балкана” гр.Борово /2015г./, XIV МФФ „Адриатика 2016” гр.Будва, Черна гора, направени са видеозаписи на клуба от звукозаписно студио „Zvooka.BG”.
                </p>
