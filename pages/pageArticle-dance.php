            <p>
                През 2001г. се правят опити за нов вид изкуство. Под ръководството на Цветелин Ненов от Клуб за спорни танци „Фламинго” гр.Русе започват репетиции, но за съжаление не просъществуват дълго, въпреки че интересът отначало е голям.
                </p>
                <p>
                След кратко прекъсване през 2003г. детския танцов състав отново започва да функционира под ръководството на Гинка Стоянова / от Кубрат/. 
                </p>
                <p>
                От 2005г. хореографи са Недко Йорданов и Виолета Николова. Освен народните танци децата с интерес и желание се включват в групата за модерен балет „Мегаданс”. 
                </p>
                <p>
                През 2009г. читалището успява да отдели средства за платен хореограф. Като такъв е назначена Сийка Братоева - дългогодишен хореограф на ансамбъл „Найден Киров”. Детския танцов състав e наречен  „Тетовче” . Ежегодно взема участие  във ФФ „Златната гъдулка”. Децата се представят подобаващо и в Румъния – през 2009г. на  11 МФФ в гр.Търговище,  през 2011г. – МФФ „Кралят на боровете” в гр.Бистрица, а  в 2012г. „Празник на гроздето и виното” в гр.Букурещ.
                </p>
                <p>
                В 2011г. в читалището започва работа нов хореограф – Диляна Ангелова-Асланян. Много са изявите:  участие във ФФ „Златната гъдулка”, Национален фолклорен събор „Лудогорие” гр.Разград /2013г./, Фолклорен фестивал „Сандрово пее и танцува” / 2013г., 2017г./, Национален фолклорен фестивал „Море от ритми”гр. Балчик / 2013г./,Младежки фолклорен фестивал „Жива вода” с.Каран Върбовка /2015г./, Фолклорен фестивал „От Дунав до Балкана” гр.Борово / 2015Г/, Международен младежки танцов фестивал „Северина” гр.Русе / 2016г.,2017г./, IX национален събор на читалищата  в гр. Бяла, Варненска обл. и др.
                </p>
